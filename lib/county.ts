export function getCountyBySlug(slug: string): County | undefined {
  return NC_COUNTIES.find(c => c.slug === slug);
}

export function getCountyByFips(fips: string): County | undefined {
  return NC_COUNTIES.find(c => c.fips === fips);
}

export function getCountyByName(name: string): County | undefined {
  return NC_COUNTIES.find(
    c => c.name.toLowerCase() === name.toLowerCase()
  );
}

export function getAllCountySlugs(): string[] {
  return NC_COUNTIES.map(c => c.slug);
}

export function searchCounties(query: string): County[] {
  const q = query.toLowerCase();
  return NC_COUNTIES.filter(
    c => c.name.toLowerCase().includes(q) || c.slug.includes(q)
  );
}
